public class KieuKhungHinh {
    int soHang;
    int soCot;

    public KieuKhungHinh(int soHang, int soCot) {
        this.soHang = soHang;
        this.soCot = soCot;
    }

    public int getSoHang() {
        return soHang;
    }

    public void setSoHang(int soHang) {
        this.soHang = soHang;
    }

    public int getSoCot() {
        return soCot;
    }

    public void setSoCot(int soCot) {
        this.soCot = soCot;
    }
}
